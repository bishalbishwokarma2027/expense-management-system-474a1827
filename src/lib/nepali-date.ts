// Reference date: 2000/01/01 BS = 1943/04/14 AD
const BS_REF = { year: 2000, month: 0, day: 1 };
const AD_REF = new Date(1943, 3, 14); // April 14, 1943

export interface NepaliDate {
  year: number;
  month: number; // 0-indexed
  day: number;
  monthName: string;
}

export function adToBS(adDate: Date): NepaliDate {
  const diffMs = adDate.getTime() - AD_REF.getTime();
  let totalDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));

  let bsYear = BS_REF.year;
  let bsMonth = BS_REF.month;
  let bsDay = BS_REF.day;

  while (totalDays > 0) {
    const cal = BS_CALENDAR[bsYear];
    if (!cal) break;

    const daysInMonth = cal[bsMonth];
    const remaining = daysInMonth - bsDay;

    if (totalDays <= remaining) {
      bsDay += totalDays;
      totalDays = 0;
    } else {
      totalDays -= (remaining + 1);
      bsMonth++;
      if (bsMonth >= 12) {
        bsMonth = 0;
        bsYear++;
      }
      bsDay = 1;
    }
  }

  return {
    year: bsYear,
    month: bsMonth,
    day: bsDay,
    monthName: BS_MONTHS[bsMonth],
  };
}

export function formatNepaliDate(date: Date): string {
  const bs = adToBS(date);
  return `${bs.day} ${bs.monthName} ${bs.year} BS`;
}

export function formatNepaliDateFromISO(isoStr: string): string {
  return formatNepaliDate(new Date(isoStr));
}

